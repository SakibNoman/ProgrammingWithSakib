//৪) একটা ইংরেজি লেটার ইনপুট নিতে হবে যদি সেটা ছোট হাতের হয় আউটপুট দেখাতে হবে বড় হাতের, যদি ইনপুট বড় হাতের হয় আউটপুট হবে ছোট হাতের। যেমন : a ইনপুট হলে A হবে আউটপুট , B ইনপুট হলে b হবে আউটপুট

#include<stdio.h>

int main()
{

    char ch;
    scanf("%c",&ch);
    if(ch>='a' && ch<='z')
    {
        printf("%c",ch-32);
    }
    else
    {
        printf("%c",ch+32);
    }

    return 0;
}
